void asciiToLcd(int fd, const char* inputStr, int x, int y, uint16_t color) {
    pthread_mutex_lock(&lock);
    isDrawing = 1;
    pthread_mutex_unlock(&lock);
    int xNext = x;
    for (size_t k = 0; k < strlen(inputStr); k++) {
        char c = inputStr[k];
        const uint8_t (*chr)[5] = charMap[(int)c];
        for (int i = 0; i < 7; i++) {
            for (int j = 0; j < 5; j++) {
                if (chr[i][j] == 1) {
                    lcd_draw_pixel(fd, xNext + j, y + i, color);
                }
            }
        }
        xNext += 6; // give 1 extra space between chars
    }
    pthread_mutex_lock(&lock);
    isDrawing = 0;
    pthread_mutex_unlock(&lock);
}

void fillCircle(int fd, int x, int y, int radius, uint16_t color) {
    pthread_mutex_lock(&lock);
    isDrawing = 1;
    pthread_mutex_unlock(&lock);

    for (int i = -radius; i <= radius; i++) { // x coordinate
        for (int j = -radius; j <= radius; j++) { // y coordinate
            if (i*i + j*j <= radius*radius) {
                lcd_draw_pixel(fd, x + i, y + j, color);
            }
        }
    }

    pthread_mutex_lock(&lock);
    isDrawing = 0;
    pthread_mutex_unlock(&lock);
}

void fillBar(int fd, int x, int y, int xLen, int yLen, uint16_t color) {
    pthread_mutex_lock(&lock);
    isDrawing = 1;
    pthread_mutex_unlock(&lock);

    for (int i = 0; i <= xLen; i++) { // x coordinate
        for (int j = 0; j <= yLen; j++) { // y coordinate
            lcd_draw_pixel(fd, x + i, y + j, color);
        }
    }

    pthread_mutex_lock(&lock);
    isDrawing = 0;
    pthread_mutex_unlock(&lock);
}

void fillDiamond(int fd, int x, int y, int xDis, int yDis, uint16_t color) {
    pthread_mutex_lock(&lock);
    isDrawing = 1;
    pthread_mutex_unlock(&lock);

    for (int i = -xDis; i <= xDis; i++) { // x coordinate
        for (int j = -yDis; j <= yDis; j++) { // y coordinate
            if (abs(i) * yDis + abs(j) * xDis <= xDis * yDis) {
                lcd_draw_pixel(fd, x + i, y + j, color);
            }
        }
    }

    pthread_mutex_lock(&lock);
    isDrawing = 0;
    pthread_mutex_unlock(&lock);
}
